        dest.setDocumentURI("http://example.com");
        dest.setXmlStandalone(true);
        dest.setXmlVersion("1.0");
        Element element = dest.createElementNS("http://www.w3.org/1999/xhtml", "element");
        dest.appendChild(element);

        // Add an element to the namespacesStack
        Stack<String> namespacesStack = new Stack<>();
        namespacesStack.push("http://www.test.com");

        // Call the tail method
