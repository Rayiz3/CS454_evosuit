    @Test public void test6() {
        // Change the value within the CDATA section to a different value
        String xml = "<div id=1><![CDATA[\n<changed>\n <value><&amp;]]></div>";
        Document doc = Jsoup.parse(xml, "", Parser.xmlParser());

        Element div = doc.getElementById("1");
        assertEquals("<changed>\n <value><&amp;", div.text());
        assertEquals(0, div.children().size());
        assertEquals(1, div.childNodeSize()); // no elements, one text node

        assertEquals("<div id=\"1\"><![CDATA[\n<changed>\n <value><&amp;]]>\n</div>", div.outerHtml());

        CDataNode cdata = (CDataNode) div.textNodes().get(0);
        assertEquals("\n<changed>\n <value><&amp;", cdata.text());
    }
