    double[][] m2 = { { 0.5, 0.0,  0.0 },
                      { 0.0, -0.5, 0.0 },
                      { 0.0, 0.0, -0.5 } };
    r = new Rotation(m2, 1.0e-12);

    double[][] m3 = r.getMatrix();
    double d00 = m2[0][0] - m3[0][0];
    double d01 = m2[0][1] - m3[0][1];
    double d02 = m2[0][2] - m3[0][2];
    double d10 = m2[1][0] - m3[1][0];
    double d11 = m2[1][1] - m3[1][1];
